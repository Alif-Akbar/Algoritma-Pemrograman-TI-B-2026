
# functions_arguments.py menunjukkan cara mendefinisikan fungsi dengan argumen di python

def greet(name):
    print("Hello,", name)
greet("Alice")
greet("Bob")
greet("Charlie")

def add(a, b):
    return a + b
result = add(5, 10)
print("Hasil penjumlahan:", result)

def multiply(x, y):
    return x * y
result = multiply(4, 3)
print("Hasil perkalian:", result)